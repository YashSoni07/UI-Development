// indexOf
let l1 = [10,20,30,40,10,20,30,40]
//         0  1  2  3  4  5  6  7
console.log(l1.indexOf(20)) // 1
console.log(l1.indexOf(20,4)) //5

// lastIndexOf
let l2 = [10,20,30,40,10,20,30,40]
//         0  1  2  3  4  5  6  7
console.log(l2.lastIndexOf(20)) // 1
console.log(l2.lastIndexOf(20,3)) // 5